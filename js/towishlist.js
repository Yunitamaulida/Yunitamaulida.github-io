$(document).ready(function(){
    $("#wishlist_prod-1").click(function(){
        var img = $(this);
	    var src = img.attr('src');

	    if(src != 'icon/wishlistafter.png') {
	        img.attr('src','icon/wishlistafter.png');
	        img.addClass("wishlistOn");
	        return false;
	    }

	    if(src != 'icon/wishlistbefore.png') {
	        img.attr('src','icon/wishlistbefore.png');
	        img.removeClass("wishlistOn");
	        return false;
	    }
    });
});

